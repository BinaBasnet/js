function changeHeading() {
    let h1 = document.getElementById('heading');
    h1.style.background = 'black';
    h1.style.borderLeft = "20px solid green";
}

function hidePara() {
    let p = document.getElementById('para');
    p.style.display = 'none';
}

function showPara() {
    let p = document.getElementById('para');
    p.style.display = 'block';
}
